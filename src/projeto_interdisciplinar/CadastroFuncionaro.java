    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        // TODO add your handling code here:
        Funcionario func = criarFuncionario();
        String resp = new FuncionarioDAO().cadastrarFuncionario(func);
        
        if(resp.equals("OK")){
            msg.setText("Funcionario Gravado com sucesso!");
        }else{
            msg.setText(resp);
        }
    }//GEN-LAST:event_buttonCadastrarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Funcionario func = new FuncionarioDAO().consultarFuncionario(Integer.parseInt(txtCodigo.getText()));
        String salario = Double.toString(func.getSalario());
        String cod = Integer.toString(func.getCod_funcionario());
        if(func == null){
            msg.setText("Funcionario nao encontrado");
        }else{
            txtCodigo.setText(cod);
            txtBairro.setText(func.getBairro_funcionario());
            txtCelular.setText(func.getCelular_funcionario());
            txtCep.setText(func.getCep_funcionario());
            txtCpf.setText(func.getCpf_funcionario());
            txtEmail.setText(func.getEmail_funcionario());
            txtEndereco.setText(func.getEndereco_funcionario());
            txtNome.setText(func.getNome_funcionario());
            txtCargo.setText(func.getCargo());
            nrSalario.setText(salario);

            msg.setText("Funcionario encontrado");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Funcionario func = criarFuncionario();
        String resp = new FuncionarioDAO().alterarFuncionario(func);

        if(resp.equals("OK")){
            msg.setText("Funcionario alterado com sucesso");
        }else{
            msg.setText((resp));
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String resp = new FuncionarioDAO().deletarFuncionario(Integer.parseInt(txtCodigo.getText()));

        if(resp.equals("OK")){
            msg.setText("Funcionario deletado com sucesso");
        }else{
            msg.setText(resp);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

