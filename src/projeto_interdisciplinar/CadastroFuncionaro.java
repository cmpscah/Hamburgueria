    public Funcionario criarFuncionario(){
        Funcionario func = new Funcionario();
        func.setBairro_funcionario(txtBairro.getText());
        func.setCargo(txtCargo.getText());
        func.setCelular_funcionario(txtCelular.getText());
        func.setCep_funcionario(txtCelular.getText());
        func.setCod_funcionario(Integer.parseInt(txtCodigo.getText()));
        func.setCpf_funcionario(txtCpf.getText());
        func.setEmail_funcionario(txtEmail.getText());
        func.setEndereco_funcionario(txtEndereco.getText());
        func.setNome_funcionario(txtNome.getText());
        func.setSalario(Double.parseDouble(nrSalario.getText()));
        
        return func;
    }

