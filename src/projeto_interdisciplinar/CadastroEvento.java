    public Evento criarEvento() {
        Evento evento = new Evento();

        evento.setCod_evento(Integer.parseInt(txtCodigo.getText()));
        evento.setAtracoes(txtAtracoes.getText());       
        evento.setNome_tema(txtTema.getText());
        evento.setNome_evento(txtNomeEvent.getText());
        evento.setValor_ingresso(Double.parseDouble(txtPrecoIngre.getText()));

        return evento;
    }

