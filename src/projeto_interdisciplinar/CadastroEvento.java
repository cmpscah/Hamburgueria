    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        // TODO add your handling code here:
        Evento evento = criarEvento();
        String resp = new EventoDAO().cadastrarEvento(evento);

        if (resp.equals("OK")) {
            msg.setText("Evento Gravado com sucesso!");
        } else {
            msg.setText(resp);
        }
    }//GEN-LAST:event_buttonCadastrarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Evento evento = new EventoDAO().consultarEvento(Integer.parseInt(txtCodigo.getText()));
        String valorIngresso = Double.toString(evento.getValor_ingresso());
        String cod = Integer.toString(evento.getCod_evento());
        if(evento == null){
            msg.setText("Evento nao encontrado");
        }else{
            txtCodigo.setText(cod);
            txtAtracoes.setText(evento.getAtracoes());
            txtNomeEvent.setText(evento.getNome_evento());
            txtPrecoIngre.setText(valorIngresso);
            txtTema.setText(evento.getNome_tema());

            msg.setText("Evento encontrado");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Evento evento = criarEvento();
        String resp = new EventoDAO().alterarEvento(evento);

        if(resp.equals("OK")){
            msg.setText("Evento alterado com sucesso");
        }else{
            msg.setText((resp));
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String resp = new EventoDAO().deletarEvento(Integer.parseInt(txtCodigo.getText()));

        if(resp.equals("OK")){
            msg.setText("Evento deletado com sucesso");
        }else{
            msg.setText(resp);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

