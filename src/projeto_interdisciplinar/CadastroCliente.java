    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Cliente cliente = new ClienteDAO().consultarCliente(Integer.parseInt(txtCodigo.getText()));
        if(cliente == null){
            msg.setText("Cliente nao encontrado");
        }else{
            txtBairro.setText(cliente.getBairro_cliente());
            txtCelular.setText(cliente.getCelular_cliente());
            txtCep.setText(cliente.getCep_cliente());
            txtCpf.setText(cliente.getCpf_cliente());
            txtEmail.setText(cliente.getEmail_cliente());
            txtEndereco.setText(cliente.getEndereco_cliente());
            txtNome.setText(cliente.getNome_cliente());
            
            msg.setText("Cliente encontrado");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        Cliente cliente = criarCliente();
        String resp = new ClienteDAO().alterarCliente(cliente);
        
        if(resp.equals("OK")){
            msg.setText("Cliente alterado com sucesso");            
        }else{
            msg.setText((resp));
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String resp = new ClienteDAO().deletarCliente(Integer.parseInt(txtCodigo.getText()));
        
        if(resp.equals("OK")){
            msg.setText("Cliente deletado com sucesso");
        }else{
            msg.setText(resp);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

