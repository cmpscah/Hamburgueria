    private Cliente criarCliente(){
        Evento evento = new Evento();
        Funcionario func = new Funcionario();
        evento.setCod_evento(Integer.parseInt(codEvento.getText()));
        func.setCod_funcionario(Integer.parseInt(codFunc.getText()));
        
        Cliente cliente = new Cliente();
        cliente.setCod_cliente(Integer.parseInt(txtCodigo.getText()));
        cliente.setNome_cliente(txtNome.getText());
        cliente.setCpf_cliente(txtCpf.getText());
        cliente.setBairro_cliente(txtBairro.getText());
        cliente.setCelular_cliente(txtCelular.getText());
        cliente.setCep_cliente(txtCep.getText());
        cliente.setEmail_cliente(txtEmail.getText());
        cliente.setEndereco_cliente(txtEndereco.getText());
        
        cliente.setEvento_cliente(evento);
        cliente.setFunc_cliente(func);
        
        return cliente;
    }
        
        

