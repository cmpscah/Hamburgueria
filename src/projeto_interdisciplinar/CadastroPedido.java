    public String valorDoPedido(){
        double a = Double.parseDouble(txtPrecoPrato.getText());
        double b = Double.parseDouble(qtdPrato.getText());
        double resultado = a * b;
        String str = Double.toString(resultado);
        txtValorTotal.setText(str = Double.toString(resultado));
        
        return str;
      
}
    
    public Pedido criarPedido(){
        Prato prato = new Prato();
        Cliente cliente = new Cliente();
        prato.setCod_prato(Integer.parseInt(codPrato.getText()));
        prato.setValor_prato(Double.parseDouble(valorDoPedido()));
        cliente.setCod_cliente(Integer.parseInt(codCliente.getText()));     
        
        Pedido ped = new Pedido();
        ped.setCod_pedido(Integer.parseInt(txtCodigo.getText()));
        ped.setPratoPedido(prato);
        ped.setClientePedido(cliente);
        ped.setValor_pedido(Double.parseDouble(txtValorTotal.getText()));
        
        return ped;
    }

