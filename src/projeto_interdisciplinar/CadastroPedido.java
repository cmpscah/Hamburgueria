    private void buttonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCadastrarActionPerformed
        // TODO add your handling code here:
        Pedido pedido = criarPedido();
        String resp = new PedidoDAO().cadastrarPedido(pedido);
        
        if(resp.equals("OK")){
            msg.setText("Pedido Gravado com sucesso!");
        }else{
            msg.setText(resp);
        }
    }//GEN-LAST:event_buttonCadastrarActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Pedido pedido = new PedidoDAO().consultarPedido(Integer.parseInt(txtCodigo.getText()));
        String valorPedido = Double.toString(pedido.getValor_pedido());
        String cod = Integer.toString(pedido.getCod_pedido());
        if(pedido == null){
            msg.setText("Pedido nao encontrado");
        }else{
            txtCodigo.setText(cod);
            txtPrecoPrato.setText(valorPedido);
            msg.setText("Pedido encontrado");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Pedido pedido = criarPedido();
        String resp = new PedidoDAO().alterarPedido(pedido);

        if(resp.equals("OK")){
            msg.setText("Pedido alterado com sucesso");
        }else{
            msg.setText((resp));
        }
    }//GEN-LAST:event_jButton5ActionPerformed

